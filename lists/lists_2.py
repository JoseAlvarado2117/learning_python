
shoppingList = ['apples', 'bananas', 'cherries', 'dates', 'eggplant']
print(shoppingList)

# changing a value in a list
shoppingList[2] = 'cucubers'

print(shoppingList)

# using negative numbers as index for the list
print(shoppingList[-1])
print(shoppingList[-2])
print(shoppingList[-3])
print(shoppingList[-4])

# determining the number of element in a list: The len() function
nElements = len(shoppingList)
print(shoppingList)
print(nElements)